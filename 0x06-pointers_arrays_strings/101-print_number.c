#include "main.h"

/**
 * print_number - function that prints an integer.
 * @n :The integer to be printed
 * Return: outputs an integer
 */

void print_number(int n)
{
	unsigned int n1;

	if (n < 0)
	{
		_putchar('-');
		n1 = -n;
	}

	if (n1 / 10 != 0)
	{
		print_number(n1 / 10);
	}

	_putchar((n1 % 10) + '0');
}
